export async function fetchAndSeedDrugLibrary(): Promise<DrugLibraryItem[]> {
  try {
    const snap = await getDocs(collection(db, "drugLibrary"));
    if (!snap.empty) {
      const items: DrugLibraryItem[] = [];
      snap.forEach((docSnap) => {
        const d = docSnap.data() as DrugLibraryItem;
        items.push({
          ...d,
          id: docSnap.id,
          activeIngredient: d.genericName ? `${d.genericName} ${d.strength || ""}`.trim() : d.name,
          requiresPrescription: d.isPrescriptionOnly ?? d.requiresPrescription ?? false,
          emoji: d.emoji || "💊"
        });
      });
      return items;
    }

    // Collection is empty: perform self-seeding
    console.log("Seeding drugLibrary collection into Firestore with 30 NAFDAC/WHO entries...");
    for (const item of DRUG_LIBRARY) {
      await setDoc(doc(db, "drugLibrary", item.id), item);
    }
    return DRUG_LIBRARY;
  } catch (err) {
    console.warn("Could not fetch/seed drugLibrary from Firestore, using local fallback:", err);
    return DRUG_LIBRARY;
  }
}
